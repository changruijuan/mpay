public class Coin {

	private int tid;
	private int uid;
	private int money;

	public int getTid() {
		return tid;
	}

	public int getUid() {
		return uid;
	}

	public int getMoney() {
		return money;
	}

	public void setTid(int tid) {
		this.tid = tid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public void setMoney(int money) {
		this.money = money;
	}
}
